#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void*))
{
	size_t i;

	i = ft_lstsize(lst);
	while (i > 0)
	{
		f(lst->content);
		lst = lst->next;
		i--;
	}
}

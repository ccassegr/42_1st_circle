#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list *new;

	if (!(new = ft_wrmalloc(1 * sizeof(t_list))))
		return (NULL);
	new->content = content;
	new->next = NULL;
	return (new);
}

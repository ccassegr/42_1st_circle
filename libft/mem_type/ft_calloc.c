#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	char	*str;
	size_t	i;
	size_t	j;

	j = 0;
	i = count * size;
	if (!(str = (ft_wrmalloc(i))))
		return (NULL);
	while (j < i)
	{
		str[j] = 0;
		j++;
	}
	return ((void *)str);
}

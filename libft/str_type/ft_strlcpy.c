#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t i;
	size_t sze;

	if (dst == NULL || src == NULL)
		return (0);
	sze = ft_strlen(src);
	i = 0;
	if (size == 0)
		return (sze);
	while (i < size - 1 && src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (sze);
}
